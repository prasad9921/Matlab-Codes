clc;
close all;
a = 1;
b = [1 -1];
n3 = 0:101;
[x11,nx11] = stepseq(0,0,101);
[x12,nx12] = stepseq(100,0,101);
x13 = x11-x12;
x3 = sin(pi*n3/25).*x13;
y3 = filter(b,a,x3);
Hf_1 = figure;
set(Hf_1,'NumberTitle','off','Name','P0220c');
Hs = stem(n3,y3,'filled');
set(Hs,'markersize',2);
axis([-5,105,-0.15,0.15]);
ytick = [-0.15:0.05:0.15];
xlabel('n');
ylabel('(n)');
title('Output response for sinusoidal pulse');
set(gca,'YTickMode','manual','YTick',ytick);